        <h2>{{ fromLocation.name }} 、 {{ toLocation.name }} 包車接送常見問題</h2>
        <h5>Q1: 包車是否能到府接送？</h5>
        <p>可以。包車就是免去自駕的困擾，直接在家到府或者指定地點上下車接送。</p>
        <h5>Q2: 租車所附之駕駛，素質如何？</h5>
        <p>租車所提供之駕駛，皆具長期包車經驗且無負評之駕駛，且曾受負評之司機，概不派用。</p>
        <h5>Q3: 所提供之車輛，是否合法？</h5>
        <p>租車所提供之車輛，皆為合法營業之租賃車或多元計程車，絕非無保險理賠隻白牌車(車牌A開頭或B開頭)。</p>
        <h5>Q4: 司機誤餐費、司機住宿費、停車費及燃料費是否包含？</h5>
        <p>報價時，會將價格是否包含司機誤餐費、住宿費、停車費、燃料費、過路費等，詳細記載，亦絕不會於用車過程當中臨時加價，造成爭議。</p>
        <h5>Q5: 包車司機小費是否強制要給？</h5>
        <p>小費為一種鼓勵性質的額外補貼，是否給予，以司機包車過程中，是否得到客人滿意為主，若客人們覺得司機真的服務周到且辛苦，都可以給予小費鼓勵哦!!</p>
        <h5>Q6: 詢問租車附駕駛報價需要提供哪些資訊？</h5>
        <p></p>
        <ul>
          <li>日期(確認是否還有車)</li>
          <li>人數(確認安排車款是否可以容納)</li>
          <li>出發位置(確認安排哪個地區的車子)</li>
          <li>大致行程及用車天數(確認行程遠近、路程長度，確認司機住宿位置費用等)</li>
        </ul>
        <h5>Q7: 包車行程可以自訂嗎？</h5>
        <p>行程皆由客人自行提供，網站上的景點路線，主要為建議及參考，駕駛主要按照客人提供之路線行駛，當然若路線不順，也會提供專業建議，節省交通時間之浪費。</p>
        <h5>Q8: 每天用車時數，若無使用完畢是否能退費？</h5>
        <p>包車為預約制，租車所附之駕駛於行程出發後，司機的工作時間皆已保留給客人，若因為客人行程提早而結束用車，司機也無法另外安排其他客人，因此不會另外退費哦!!</p>
        <h5>Q9: 發票問題</h5>
        <p>可以開發票(二聯或三聯)，包車所提供的報價皆以未稅提供</p>
        <h5>Q10: 訂車是否需先預付訂金？</h5>
        <p>訂車皆須預付訂金才完成訂車程序</p>
        <h5>Q11: 付款方式？</h5>
        <p>線上支付或轉帳匯款</p>
        <h5>Q12: 如確認需要租車要如何完成租車手續流程？</h5>
        <p>如確認租車，客服專員會傳確認訂車單並於線上填寫，訂車單上會標註詳細的訂車資料，如用車日期時間及當天行程，及需付的訂金及匯款帳號，即完成訂車手續了</p>
        <h5>Q13: 需於提早多久時間，確認訂車？</h5>
        <p>預訂車輛建議一個月前，才免於沒車可訂哦!</p>
